/**
 * Get a language by its code
 * @param code The language code to search for
 * @returns The language object or undefined if not found
 */
export const getLanguageByCode = (code: string) => {
  return supportedLanguages.find(lang => lang.code === code);
};

/**
 * Get a language by its name
 * @param name The language name to search for
 * @returns The language object or undefined if not found
 */
export const getLanguageByName = (name: string) => {
  return supportedLanguages.find(lang => lang.name.toLowerCase() === name.toLowerCase());
};

/**
 * Get all languages for a specific region/country
 * @param countryCode The country code (e.g., 'US', 'IN')
 * @returns Array of languages for the specified country
 */
export const getLanguagesByCountry = (countryCode: string) => {
  return supportedLanguages.filter(lang => {
    const parts = lang.code.split('-');
    return parts.length > 1 && parts[parts.length - 1] === countryCode;
  });
};

